    private void btnaddappActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddappActionPerformed
        // TODO add your handling code here:
        String patientID = txt_patientID.getText();
        String appointmentID = txt_appointmentID.getText();
        Date appointment_date = jDateChooser1.getDate();
        int upper_limit_bp = Integer.parseInt(txt_upperbp.getText());
        int lower_limit_bp = Integer.parseInt(txt_lowerbp.getText());
        int heart_rate = Integer.parseInt(txt_heartrate.getText());
        
        appointment a = appointmenthistory.addNewappointment();
        
        a.setPatientID(patientID);
        a.setAppointmentID(appointmentID);
        a.setDatetime(appointment_date);
        a.setLowerlimit_bloodPressure(lower_limit_bp);
        a.setUpperlimit_bloodPressure(upper_limit_bp);
        a.setHeart_rate(heart_rate);
        
        JOptionPane.showMessageDialog(this, "Apppointment booked!");
        
        //a.setPatientID(patientID);
        txt_appointmentID.setText("");
        jDateChooser1.setDate(null);
        txt_lowerbp.setText("");
        txt_upperbp.setText("");
        txt_heartrate.setText("");
        
       
    }//GEN-LAST:event_btnaddappActionPerformed

    private void btnviewappActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnviewappActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = PersonHistoryTBT.getSelectedRow();
        
        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this,"Select a row to view all details");
            
        }
        else{
        DefaultTableModel model = (DefaultTableModel)PersonHistoryTBT.getModel();
        cars selectedperson = (cars)model.getValueAt(selectedRowIndex,0);
        
        String age = String.valueOf(selectedperson.getAge());
        txt_patientID.setText(selectedperson.getPersonID());
        txt_Patient_name.setText(selectedperson.getName());
        txt_age.setText(age);
        txt_gender.setText(selectedperson.getGender());
        txt_house.setText(selectedperson.getHouse());
        txt_community.setText(selectedperson.getCommunity());
        txt_city.setText(selectedperson.getCity());
        }
    }//GEN-LAST:event_btnviewappActionPerformed

