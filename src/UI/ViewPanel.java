        // TODO add your handling code here:
        int selectedRow = tablecars.getSelectedRow();
        
        if (selectedRow<0){
            JOptionPane.showMessageDialog(this,"Select a row to view all details");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel)tablecars.getModel();
        cars selectedcars =(cars)model.getValueAt(selectedRow, 0);
        
        
        data.deletecars(selectedcars);
        populateTable();
        
