    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)tablecars.getModel();
       model.setRowCount(0);
       
       for(cars c : data.getData()){
           Object[] row = new Object[7];
           
           row[0] = c;
           row[1] = c.getName();
           row[2] = c.getAge();
           row[3] = c.getGender();
           row[4] = c.getHouse();
           row[5] = c.getCommunity();
           row[6] = c.getCity();
           
           model.addRow(row);
           
       }
        
    }
